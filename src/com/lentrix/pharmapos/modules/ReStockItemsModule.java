    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(validateEntry()) {
            try {
                int qty = Integer.parseInt(quantityField.getText());
                float purchaseAmount = Float.parseFloat(purchaseAmountField.getText());
                float sellingPrice = Float.parseFloat(sellingPriceField.getText());
                Stock stock = new Stock(-1, selectedItem, qty, purchaseAmount, qty, sellingPrice, java.sql.Date.valueOf(LocalDate.now()));
                stock.save(user);
                int res = JOptionPane.showConfirmDialog(this, "Re-stocking finished."
                        + "\nDo you want to re-stock another?", "Again?", JOptionPane.YES_NO_OPTION);
                if(res == JOptionPane.YES_OPTION) {
                    selectedItem = null;
                    itemField.setText(null);
                    genericNameField.setText(null);
                    quantityField.setText(null);
                    purchaseAmountField.setText(null);
                    sellingPriceField.setText(null);
                    barCodeField.grabFocus();
                }else {
                    this.dispose();
                }
            }catch(NumberFormatException ex) {
                Helper.error(this, "You have an invalid numberic entry."
                        + "\nPlease check your entries and try again.");
            }catch(SQLException ex) {
                Helper.error(this, ex.getMessage());
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

