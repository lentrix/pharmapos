    private boolean validateEntry() {
        if(selectedItem==null) {
            Helper.error(this, "You have not selected an item to restock.");
            return false;
        }
        if(quantityField.getText().isEmpty() 
                || purchaseAmountField.getText().isEmpty()
                || sellingPriceField.getText().isEmpty()) {
            Helper.error(this, "You must not leave any of the fields empty.");
            return false;
        }
        
        return true;
    }
