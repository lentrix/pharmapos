    private boolean validateEntry() {
        return !nameField.getText().isEmpty()
                && !genericNameField.getText().isEmpty()
                && !barCodeField.getText().isEmpty()
                && !manufacturerField.getText().isEmpty()
                && !discountRateField.getText().isEmpty();
    }
    
    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        if(!validateEntry()) {
            Helper.error(this, "There is an error in your entry."
                    + "\nPlease check and make sure no field is empty.");
            return;
        }
        try {
            if(selectedItem==null) {
                selectedItem = new Item(
                        -1, nameField.getText(), genericNameField.getText(), 
                        barCodeField.getText(), manufacturerField.getText(),
                        Float.valueOf(discountRateField.getText())

                );
            }else {
                selectedItem.setName(nameField.getText());
                selectedItem.setGeneric(genericNameField.getText());
                selectedItem.setBarCode(barCodeField.getText());
                selectedItem.setManufacturer(manufacturerField.getText());
                selectedItem.setDisc(Float.valueOf(discountRateField.getText()));
            }
            selectedItem.save();
            Helper.info(this, "The item has been saved successfully!");
            refreshTable();
        }catch(SQLException ex) {
            Helper.error(this, ex.getMessage());
            ex.printStackTrace();
        }catch(NumberFormatException ex) {
            Helper.error(this, "Invalid entry for a numeric field.");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        selectedItem = (Item)jTable1.getValueAt(jTable1.getSelectedRow(), 0);
        refreshForm();
    }//GEN-LAST:event_jTable1MouseClicked

    private void barcodeSearchFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_barcodeSearchFieldActionPerformed
        String barCode = barcodeSearchField.getText();
        
        for(int i=0; i<items.size(); i++) {
            Item itm = items.get(i);
            if(itm.getBarCode().equals(barCode)){
                selectedItem = itm;
                refreshForm();
                return;
            }
        }
        Helper.error(this, "Bar Code not found on the records!");
    }//GEN-LAST:event_barcodeSearchFieldActionPerformed

