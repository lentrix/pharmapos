    private void refreshForm() {
        nameField.setText(selectedItem.getName());
        genericNameField.setText(selectedItem.getGeneric());
        barCodeField.setText(selectedItem.getBarCode());
        manufacturerField.setText(selectedItem.getManufacturer());
        discountRateField.setText(String.valueOf(selectedItem.getDisc()));
        nameField.grabFocus();
    }
