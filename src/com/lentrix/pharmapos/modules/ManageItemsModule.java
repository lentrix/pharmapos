    private void refreshTable() {
        try {
            items = Item.getItems(filterNameField.getText());
            int rows = items.size();
            
            Object[][] content = new Object[rows][4];
            String[] headers = {"Name","Generic Name", "Manufacturer","Discount Rate"};
            
            for(int i=0; i<rows; i++) {
                Item item = items.get(i);
                content[i][0] = item;
                content[i][1] = item.getGeneric();
                content[i][2] = item.getManufacturer();
                content[i][3] = (item.getDisc()*100) + "%";
            }
            
            DefaultTableModel model = new DefaultTableModel(content, headers) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            
            jTable1.setModel(model);
            jTable1.getColumnModel().getColumn(3).setMaxWidth(50);
            
        }catch(SQLException ex) {
            Helper.error(this, ex.getMessage());
            ex.printStackTrace();
        }
    }
